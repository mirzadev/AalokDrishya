import React, { useState } from "react";
import "./PortfolioStyles.css";
//import "./PortfolioItems";

const CarouselGallery = () => {
  // Array to hold image file paths
  const images = [
    "/Images/Wedding-1.jpg",
    "/Images/Wedding-2.jpg",
    "/Images/Wedding-3.jpg",
    "/Images/Wedding-4.jpg",
    "/Images/Wedding-5.jpg",
    "/Images/Wedding-6.jpg",
    "/Images/Wedding-7.jpg",
    "/Images/Wedding-8.jpg",
    "/Images/Wedding-9.jpg",
    "/Images/Wedding-10.jpg",
  ];

  const [isModalOpen, setIsModalOpen] = useState(false); // State for modal open/close
  const [currentImageIndex, setCurrentImageIndex] = useState(0); // Index for current image in modal

  // Open modal and set the selected image
  const openModal = (index) => {
    setIsModalOpen(true);
    setCurrentImageIndex(index);
  };

  // Close modal
  const closeModal = () => {
    setIsModalOpen(false);
  };

  // Go to the next image
  const nextImage = () => {
    setCurrentImageIndex((prevIndex) => (prevIndex + 1) % images.length);
  };

  // Go to the previous image
  const prevImage = () => {
    setCurrentImageIndex(
      (prevIndex) => (prevIndex - 1 + images.length) % images.length
    );
  };

  return (
    <div className="App">
      <h2>Image Gallery</h2>

      {/* Display the image thumbnails in a grid */}
      <div className="gallery">
        {images.map((image, index) => (
          <div
            className="thumbnail"
            key={index}
            onClick={() => openModal(index)}
          >
            <img src={image} alt={`thumbnail-${index}`} />
          </div>
        ))}
      </div>

      {/* Modal for image enlargement */}
      {isModalOpen && (
        <div className="modal">
          <div className="modal-content">
            {/* Back Arrow */}
            <button className="arrow-button prev" onClick={prevImage}>
              &lt;
            </button>

            <img
              src={images[currentImageIndex]}
              alt={`modal-img-${currentImageIndex}`}
              className="modal-image"
            />

            {/* Next Arrow */}
            <button className="arrow-button next" onClick={nextImage}>
              &gt;
            </button>

            <button className="close" onClick={closeModal}>
              X
            </button>
          </div>
        </div>
      )}
    </div>
  );
};

export default CarouselGallery;
